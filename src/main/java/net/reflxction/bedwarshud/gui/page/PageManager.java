package net.reflxction.bedwarshud.gui.page;

/**
 * Class which manages moving between pages
 */
class PageManager {

    Page getPageByIndex(int index) {
        for (Page page : PageRegistry.getPages()) {
            if (page.getIndex() == index) return page;
        }
        return null;
    }

}
